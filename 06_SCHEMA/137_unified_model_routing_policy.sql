CREATE TABLE IF NOT EXISTS lucidota_runtime.model_routing_policy (
    id uuid PRIMARY KEY DEFAULT gen_random_uuid(),
    workload_type text NOT NULL CHECK(workload_type IN ('embed', 'codegen', 'sql', 'reasoning', 'ner', 'analysis', 'orchestration')),
    provider text NOT NULL CHECK(provider IN ('groq', 'codestral', 'ministral', 'mamba_ram', 'mamba_gpu', 'deepseek', 'bonsai_ternary', 'bge_embed', 'local_llama')),
    priority smallint NOT NULL DEFAULT 5 CHECK(priority BETWEEN 1 AND 10),
    health text NOT NULL DEFAULT 'ok' CHECK(health IN ('ok', 'degraded', 'offline')),
    success_rate numeric(5, 4) DEFAULT 0.5000,
    bandit_weight numeric(8, 4) DEFAULT 1.0000,
    latency_p50_ms integer DEFAULT NULL,
    cost_per_1k_tokens numeric(10, 6) DEFAULT NULL,
    always_hot boolean DEFAULT FALSE,
    vram_mb_required integer DEFAULT 0,
    notes text,
    last_updated timestamptz DEFAULT now(),
    UNIQUE (workload_type, provider)
);

