def select_action(actions: list[BanditAction]) -> BanditAction:
    """ε‑greedy selection based on expected_reward."""
    eps = 0.1
    if random.random() < eps:
        return random.choice(actions)
    # pick max expected reward
    return max(actions, key=lambda a: a.expected_reward)

def record_bandit_update(update: BanditUpdate) -> None:
    """Update the global policy with a new observation."""
    total, count = _POLICY[update.action_id]
    _POLICY[update.action_id][0] = total + update.reward
    _POLICY[update.action_id][1] = count + 1

def build_pheromone_and_logratio(n: int) -> tuple[np.ndarray, np.ndarray]:
    """
    Construct synthetic pheromone and log‑count‑ratio matrices from the bandit
    policy.  For demonstration we map action counts onto a square matrix.
    """
    pheromone = np.zeros((n, n), dtype=float)
    log_ratio = np.zeros((n, n), dtype=float)
    actions = list(_POLICY.keys())
    for idx, act in enumerate(actions):
        i = idx % n
        j = (idx // n) % n
        cnt = _count(act)
        pheromone[i, j] = cnt  # raw count as pheromone strength
        # avoid log(0); add 1
        log_ratio[i, j] = math.log(cnt + 1) - math.log(1)  # = log(cnt+1)
    return pheromone, log_ratio

