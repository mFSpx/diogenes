def compute_fluxes(conductance_mat: np.ndarray,
                   edge_len_mat: np.ndarray,
                   pressure_vec: np.ndarray) -> np.ndarray:
    """
    Compute the flux on every directed edge (i → j) using the Physarum rule.
    Returns a matrix Q where Q[i, j] = flux from i to j (zero if no edge).
    """
    n = conductance_mat.shape[0]
    flux_mat = np.zeros_like(conductance_mat, dtype=float)
    for i in range(n):
        for j in range(n):
            g = conductance_mat[i, j]
            if g > 0:
                l = edge_len_mat[i, j]
                q = flux(g, l, pressure_vec[i], pressure_vec[j])
                flux_mat[i, j] = q
    return flux_mat

def update_transition_matrix(A: np.ndarray,
                             pheromone_mat: np.ndarray,
                             log_count_ratio_mat: np.ndarray,
                             alpha: float = 0.1) -> np.ndarray:
    """
    Apply pheromone‑infotaxis to the shared transition / conductance matrix.
    The update is element‑wise:
        A_new = A + α * (pheromone ⊙ log_count_ratio)
    where ⊙ denotes element‑wise multiplication.
    """
    infotaxis = _pheromone_infotaxis(pheromone_mat, log_count_ratio_mat)
    return A + alpha * infotaxis

def hybrid_step(state: np.ndarray,
                A: np.ndarray,
                conductance_mat: np.ndarray,
                edge_len_mat: np.ndarray,
                pressure_vec: np.ndarray,
                pheromone_mat: np.ndarray,
                log_count_ratio_mat: np.ndarray,
                dt: float = 1.0) -> tuple[np.ndarray, np.ndarray, np.ndarray]:
    """
    Perform one hybrid iteration:
      1. Compute Physarum fluxes Q.
      2. Update conductances using Q (Physarum growth rule).
      3. Update the shared matrix A via pheromone infotaxis.
      4. Propagate the state: x_{t+1} = A @ x_t + Σ_i Q[:, i] (total inflow).

    Returns the tuple (new_state, new_A, new_conductance_mat).
    """
    # 1. Fluxes
    Q = compute_fluxes(conductance_mat, edge_len_mat, pressure_vec)

    # 2. Conductance update (edge‑wise)
    new_conductance = np.copy(conductance_mat)
    n = conductance_mat.shape[0]
    for i in range(n):
        for j in range(n):
            g = conductance_mat[i, j]
            if g > 0:
                new_conductance[i, j] = update_conductance(g, Q[i, j], dt=dt)

    # 3. Transition matrix update (pheromone side)
    new_A = update_transition_matrix(A, pheromone_mat, log_count_ratio_mat)

    # 4. State propagation
    # Input term: net inflow to each node = Σ_j Q[j, i] - Σ_j Q[i, j]
    inflow = Q.sum(axis=0) - Q.sum(axis=1)
    new_state = new_A @ state + inflow

    return new_state, new_A, new_conductance

