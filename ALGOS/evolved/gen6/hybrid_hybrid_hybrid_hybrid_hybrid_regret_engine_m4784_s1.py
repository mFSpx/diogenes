def gini_coefficient(values: np.ndarray) -> float:
    xs = np.sort(values)
    if xs.size == 0 or np.sum(xs) == 0: return 0.0
    if xs[0] < 0: raise ValueError("values must be non-negative")
    n = xs.size
    return np.sum((2 * np.arange(n) - n + 1) * xs) / (n * np.sum(xs))

