def estimate_rlct(train_losses_per_n: List[float], n_values: List[float]) -> float:
    """
    Perform the log‑log regression used in Algorithm A to obtain the RLCT exponent.
    Returns a single float scaling factor.
    """
    losses = np.asarray(train_losses_per_n, dtype=np.float64)
    ns = np.asarray(n_values, dtype=np.float64)

    if np.any(ns <= np.e):
        raise ValueError("All n_values must be > e for log(log(n)) to be positive")
    if losses.shape != ns.shape:
        raise ValueError("train_losses_per_n and n_values must have the same length")

    y = np.log(np.maximum(losses, 1e-300))
    x = np.log(np.log(ns))

    x_c = x - x.mean()
    y_c = y - y.mean()
    var_x = (x_c ** 2).sum()
    if var_x < 1e-15:
        raise ValueError("n_values have no variance in log(log(n)) space")

    return float((x_c * y_c).sum() / var_x)


def calculate_pheromone_signal(
    pheromone_store: Dict[str, Dict[str, float]],
    surface_key: str,
    signal_kind: str,
    signal_value: float,
    half_life_seconds: float,
    elapsed_seconds: float = 1.0,
) -> float:
    """
    Initialise or retrieve a pheromone signal and apply exponential decay.
    """
    if surface_key not in pheromone_store:
        pheromone_store[surface_key] = {}
    store = pheromone_store[surface_key]

    if signal_kind not in store:
        store[signal_kind] = signal_value

    decay_factor = math.pow(0.5, elapsed_seconds / half_life_seconds)
    store[signal_kind] *= decay_factor
    return store[signal_kind]

