def compute_fused_health(
    nodes: Dict[str, Point],
    edges: List[Edge],
    root: str,
    counts: Dict[str, int],
    failures: Dict[str, int],
    failure_threshold: float,
    unique_qi: Dict[str, int],
    total_records: int,
    recovery_priority: Dict[str, float],
    rlct_factor: float,
    pheromone_store: Dict[str, Dict[str, float]],
) -> Dict[str, float]:
    """
    Compute the fused health for each node using:
    - RLCT scaling (rlct_factor)
    - A multivector magnitude built from pheromone strength and edge length
    - Privacy‑risk and failure terms from Algorithm B
    Returns a dict mapping node identifiers to raw health values.
    """
    _, edge_lengths, root_distances = tree_metrics(nodes, edges, root)

    healths: Dict[str, float] = {}
    for node in nodes:
        # ----- privacy / failure terms -----
        recon_risk = unique_qi.get(node, 0) / total_records
        fail_rate = failures.get(node, 0) / failure_threshold
        term1 = 1.0 - (recon_risk * fail_rate)
        term2 = 1.0 - recovery_priority.get(node, 0.0)

        # ----- count term -----
        cnt = counts.get(node, 0)
        log_count = math.log(cnt + 1)

        # ----- multivector magnitude (approximation) -----
        # Use pheromone strength on the node's surface as one component,
        # and the distance from root as the other.
        pheromone_val = calculate_pheromone_signal(
            pheromone_store,
            surface_key=node,
            signal_kind="signal",
            signal_value=1.0,          # initialise with unit strength if absent
            half_life_seconds=30.0,
            elapsed_seconds=1.0,
        )
        distance = root_distances.get(node, 0.0)
        mv_norm = math.hypot(pheromone_val, distance)  # |mv| = sqrt(p^2 + d^2)

        # ----- fused health -----
        raw = rlct_factor * mv_norm * term1 * term2 * log_count
        healths[node] = max(raw, 0.0)  # enforce non‑negative health

    return healths


def allocate_work_share(healths: Dict[str, float]) -> Dict[str, float]:
    """
    Normalise raw health values into work‑share fractions that sum to 1.
    """
    total = sum(healths.values())
    if total == 0.0:
        # If all healths are zero, distribute uniformly
        n = len(healths)
        return {k: 1.0 / n for k in healths}
    return {k: v / total for k, v in healths.items()}


def update_pheromone_store(
    pheromone_store: Dict[str, Dict[str, float]],
    decay_seconds: float = 1.0,
) -> None:
    """
    Apply a global decay step to all stored pheromone signals.
    """
    for surface_key, signals in pheromone_store.items():
        for kind, val in list(signals.items()):
            # Use a fixed half‑life of 30 s for simplicity
            half_life = 30.0
            decay_factor = math.pow(0.5, decay_seconds / half_life)
            signals[kind] = val * decay_factor

