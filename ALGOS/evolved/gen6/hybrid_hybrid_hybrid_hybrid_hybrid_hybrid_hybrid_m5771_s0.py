def length(a: Point, b: Point) -> float:
    """Euclidean distance between two points."""
    return math.hypot(a[0] - b[0], a[1] - b[1])


def tree_metrics(
    nodes: Dict[str, Point],
    edges: List[Edge],
    root: str,
) -> Tuple[Dict[str, List[str]], Dict[Edge, float], Dict[str, float]]:
    """
    Build adjacency list, compute edge lengths, and root‑to‑node distances.
    Returns (adjacency, edge_lengths, root_distances).
    """
    adjacency: Dict[str, List[str]] = {k: [] for k in nodes}
    edge_lengths: Dict[Edge, float] = {}
    for u, v in edges:
        adjacency[u].append(v)
        adjacency[v].append(u)
        edge_lengths[(u, v)] = edge_lengths[(v, u)] = length(nodes[u], nodes[v])

    # BFS to compute root distances
    root_distances: Dict[str, float] = {root: 0.0}
    visited = {root}
    queue = [root]
    while queue:
        current = queue.pop(0)
        for neighbor in adjacency[current]:
            if neighbor not in visited:
                root_distances[neighbor] = root_distances[current] + edge_lengths[(current, neighbor)]
                visited.add(neighbor)
                queue.append(neighbor)

    return adjacency, edge_lengths, root_distances

