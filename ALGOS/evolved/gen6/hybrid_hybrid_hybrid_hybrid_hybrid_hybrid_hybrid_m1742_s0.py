class HybridSheaf:
    def __init__(self, node_dims: dict, edges: list, patterns: np.ndarray):
        self.node_dims = node_dims
        self.edges = edges
        self.patterns = patterns
        self._restrictions = {}
        self._sections = {}

    def set_restriction(self, edge: tuple, src_map: np.ndarray, dst_map: np.ndarray) -> None:
        u, v = edge
        if src_map.shape[1] != self.node_dims[u]:
            raise ValueError("src_map column dimension must match dim(node u)")
        if dst_map.shape[1] != self.node_dims[v]:
            raise ValueError("dst_map column dimension must match dim(node v)")
        if src_map.shape[0] != dst_map.shape[0]:
            raise ValueError("src_map and dst_map must have the same row dimension")
        self._restrictions[(u, v)] = (np.asarray(src_map, dtype=float), np.asarray(dst_map, dtype=float))

    def set_section(self, node: any, value: np.ndarray) -> None:
        if value.shape[0] != self.node_dims[node]:
            raise ValueError("Section dimension must match node dimension")
        self._sections[node] = np.asarray(value, dtype=float)

    def project_pattern(self, node: any, pattern: np.ndarray) -> np.ndarray:
        restrictions = self._restrictions.get((node, 'ref'))
        if restrictions:
            src_map, dst_map = restrictions
            projected_pattern = np.dot(pattern, src_map)
            projected_pattern = np.dot(projected_pattern, dst_map)
            return projected_pattern
        else:
            return np.zeros(self.node_dims[node])

class HybridSheafGini:
    def __init__(self, sheaf: HybridSheaf, gini_coefficient: float):
        self.sheaf = sheaf
        self.gini_coefficient = gini_coefficient

    def compute_burst_signal(self, pattern: np.ndarray) -> np.ndarray:
        node_dims = self.sheaf.node_dims
        num_nodes = len(node_dims)
        burst_signal = np.zeros(num_nodes)
        for node in node_dims:
            projected_pattern = self.sheaf.project_pattern(node, pattern)
            burst_signal[node] = np.sum(projected_pattern)
        return burst_signal

    def compute_gini_coefficient(self, burst_signal: np.ndarray) -> float:
        sorted_signal = np.sort(burst_signal)
        num_nodes = len(sorted_signal)
        gini = 0.0
        for i in range(1, num_nodes):
            gini += (2 * i - num_nodes - 1) * sorted_signal[i]
        gini /= (num_nodes * np.sum(sorted_signal))
        return gini

    def compute_pattern_inequality(self, pattern: np.ndarray) -> float:
        burst_signal = self.compute_burst_signal(pattern)
        return self.compute_gini_coefficient(burst_signal)

