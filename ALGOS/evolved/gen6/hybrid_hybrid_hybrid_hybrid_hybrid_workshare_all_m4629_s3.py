def simulate_hybrid_process(
    steps: int,
    init_store: float,
    morph: Morphology,
    base_date: Tuple[int, int, int],
    text_supplier,
    rng: random.Random = random,
) -> List[float]:
    """
    Run a simple simulation of the hybrid store for *steps* iterations.

    *text_supplier* is a callable returning a string for each step (e.g. a
    random sentence generator).  The date advances one day per step.
    Returns the list of store values (including the initial one).
    """
    store_history = [init_store]
    year, month, day = base_date
    for step in range(steps):
        dow = _weekday_index(year, month, day)

        # Random token pools for demonstration
        inflow = rng.random() * 100.0
        outflow = rng.random() * 80.0
        inflow_vec = allocate_tokens_by_weekday(inflow, dow)
        outflow_vec = allocate_tokens_by_weekday(outflow, dow)

        text = text_supplier()
        new_store = hybrid_store_update(
            store=store_history[-1],
            inflow_vec=inflow_vec,
            outflow_vec=outflow_vec,
            morph=morph,
            dow=dow,
            text=text,
            dt=1.0,
        )
        store_history.append(new_store)

        # advance date by one day (simple overflow handling)
        try:
            dt_obj = datetime(year, month, day) + dt.timedelta(days=1)
        except Exception:
            dt_obj = datetime(year, month, day)  # fallback, should not happen
        year, month, day = dt_obj.year, dt_obj.month, dt_obj.day

    return store_history


