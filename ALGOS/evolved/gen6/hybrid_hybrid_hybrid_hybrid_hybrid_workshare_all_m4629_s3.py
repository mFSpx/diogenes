def _stylometric_factor(text: str) -> float:
    """
    Compute a stylometric interaction factor ϕ ∈ [0.5, 1.5].

    The factor is linearly mapped from the proportion of function words in the
    supplied text: a higher proportion yields a larger ϕ.
    """
    words = [w.strip(".,!?:;\"'").lower() for w in text.split()]
    if not words:
        return 1.0
    func_cnt = sum(1 for w in words if w in _FUNCTION_WORDS)
    ratio = func_cnt / len(words)          # ∈ [0, 1]
    phi = 0.5 + ratio * 1.0                # map to [0.5, 1.5]
    return max(0.5, min(1.5, phi))


