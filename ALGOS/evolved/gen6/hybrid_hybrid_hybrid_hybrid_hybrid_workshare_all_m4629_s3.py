def allocate_tokens_by_weekday(
    total_tokens: float,
    dow: int,
    groups: Sequence[str] = GROUPS,
) -> np.ndarray:
    """
    Distribute *total_tokens* across *groups* using the weekday weight vector.
    Returns an array of shape (len(groups),) whose sum equals *total_tokens*.
    """
