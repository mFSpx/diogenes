def _weekday_index(year: int, month: int, day: int) -> int:
    """
    Return weekday index where 0 = Sunday … 6 = Saturday.
    """
    # datetime.weekday(): Monday=0 … Sunday=6 → shift
    return (datetime(year, month, day).weekday() + 1) % 7


def weekday_weight_vector(groups: Sequence[str], dow: int) -> np.ndarray:
    """
    Produce a normalized weight vector for *groups* based on the weekday ``dow``.
    The vector is generated by rotating a sinusoidal pattern; the rotation angle
    is proportional to ``dow`` (0‑6).  The result is stochastic (sums to 1).

    Mathematically:
        w_i(d) = [1 + α·sin(θ_i + φ(d))] / Σ_j[1 + α·sin(θ_j + φ(d))]
    where
        θ_i = 2π·i / N   (N = number of groups)
        φ(d) = 2π·d / 7   (weekday phase)
        α    = 0.2        (amplitude)
    """
    n = len(groups)
    theta = 2.0 * math.pi * np.arange(n) / n
    phi = 2.0 * math.pi * dow / 7.0
    raw = 1.0 + _AMPLITUDE * np.sin(theta + phi)
    weights = raw / raw.sum()
    return weights


