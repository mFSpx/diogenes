def hybrid_store_update(
    store: float,
    inflow_vec: np.ndarray,
    outflow_vec: np.ndarray,
    morph: Morphology,
    dow: int,
    text: str,
    dt: float = 1.0,
) -> float:
    """
    Perform one hybrid update step.

    Parameters
    ----------
    store: current store value (non‑negative scalar)
    inflow_vec, outflow_vec: raw token vectors of shape (n_groups,)
    morph: morphology instance providing α, β
    dow: weekday index (0 = Sunday … 6 = Saturday)
    text: arbitrary string used for stylometric factor ϕ
    dt: time step multiplier

    Returns
    -------
    Updated store value (guaranteed non‑negative).
    """
    if inflow_vec.shape != outflow_vec.shape:
        raise ValueError("inflow and outflow vectors must have the same shape")

    # Geometry coefficients
    alpha, beta = compute_geometry_coeffs(morph)

    # Stylometric interaction factor
    phi = _stylometric_factor(text)

    # Weekday‑dependent weighting
    w = weekday_weight_vector(GROUPS, dow)
    weighted_in = np.dot(w, inflow_vec)   # scalar total inflow after weighting
    weighted_out = np.dot(w, outflow_vec) # scalar total outflow after weighting

    # Hybrid differential equation
    delta = alpha * (phi * weighted_in) - beta * ((2.0 - phi) * weighted_out)

    new_store = max(0.0, store + dt * delta)
    return new_store


