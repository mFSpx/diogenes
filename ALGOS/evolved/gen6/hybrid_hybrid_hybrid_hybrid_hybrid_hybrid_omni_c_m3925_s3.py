def hoeffding_bound(n: int, delta: float = 0.05) -> float:
    """Hoeffding bound ε = sqrt( (1/(2n)) * ln(2/δ) )."""
    if n <= 0:
        return float("inf")
    return math.sqrt((1.0 / (2 * n)) * math.log(2.0 / delta))


def hoeffding_guided_update(
    observations: List[float],
    prior_mean: float,
    prior_var: float,
    delta: float = 0.05,
) -> Tuple[float, float, bool]:
    """
    Perform a Bayesian Gaussian update on the edge weight if the Hoeffding
    bound indicates sufficient samples.

    Returns (posterior_mean, posterior_var, updated_flag).
    """
    n = len(observations)
    eps = hoeffding_bound(n, delta)
    sample_mean = np.mean(observations) if n > 0 else 0.0
    sample_var = np.var(observations, ddof=1) if n > 1 else 1.0

    # If the empirical mean lies within ε of the prior mean, we consider the data
    # informative enough to update.
    if abs(sample_mean - prior_mean) <= eps:
        # Conjugate Gaussian update (known variance = sample_var)
        precision_prior = 1.0 / prior_var
        precision_lik = n / sample_var
        post_var = 1.0 / (precision_prior + precision_lik)
        post_mean = post_var * (precision_prior * prior_mean + precision_lik * sample_mean)
        return post_mean, post_var, True
    else:
        return prior_mean, prior_var, False


