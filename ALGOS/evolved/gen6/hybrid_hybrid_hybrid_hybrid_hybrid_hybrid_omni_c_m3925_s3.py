def minhash_signature(seq: List[int], num_perm: int = 64) -> np.ndarray:
    """Generate a MinHash signature for an integer sequence.
    The implementation uses random hash functions of the form
    h(x) = (a * x + b) % prime, where a,b are random."""
    prime = 2 ** 31 - 1
    max_val = max(seq) if seq else 0
    a = np.random.randint(1, prime, size=num_perm, dtype=np.int64)
    b = np.random.randint(0, prime, size=num_perm, dtype=np.int64)

    sig = np.full(num_perm, np.iinfo(np.int64).max, dtype=np.int64)
    for x in seq:
        hx = (a * x + b) % prime
        sig = np.minimum(sig, hx)
    return sig


def minhash_similarity(sig1: np.ndarray, sig2: np.ndarray) -> float:
    """Estimate Jaccard similarity from two MinHash signatures."""
    if sig1.shape != sig2.shape:
        raise ValueError("Signatures must have the same length")
    return np.mean(sig1 == sig2)


