# Shannon Entropy Calculation
def shannon_entropy(probabilities: np.ndarray) -> float:
    return -np.sum(probabilities * np.log2(probabilities))

# Signal Scores
def _byte_entropy(data: bytes, sample: int = 8192) -> float:
    if not data:
        return 0.0
    chunk = data[:sample]
    freq = np.bincount(np.frombuffer(chunk, dtype=np.uint8), minlength=256)
    probs = freq / freq.sum()
    return shannon_entropy(probs)

def signal_scores(data: bytes) -> float:
    return _byte_entropy(data)

# MinHash Similarity
def minhash_signature(input_string: str, num_hashes: int = 10) -> List[int]:
    hash_values = []
    for _ in range(num_hashes):
        hash_object = hashlib.md5(input_string.encode())
        hash_value = int(hash_object.hexdigest(), 16)
        hash_values.append(hash_value)
    return hash_values

def minhash_similarity(signature1: List[int], signature2: List[int]) -> float:
    intersection = set(signature1) & set(signature2)
    union = set(signature1) | set(signature2)
    return len(intersection) / len(union)

