def compute_regret_weighted_strategy(actions: list[MathAction], counterfactuals: list[MathCounterfactual], gini_modifier: float) -> dict[str, float]:
    if not actions: return {}
    cf = {c.action_id: c.outcome_value * c.probability for c in counterfactuals}
    vals = {a.id: (a.expected_value - a.cost - a.risk + cf.get(a.id, 0.0)) * gini_modifier for a in actions}
    best = max(vals.values())
