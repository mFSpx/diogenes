def minhash_signature(tokens: List[str], k: int = 128) -> List[int]:
    MAX64 = (1 << 64) - 1
    def _hash(seed: int, token: str) -> int:
        data = seed.to_bytes(4, 'big') + token.encode('utf-8', errors='ignore')
        return int.from_bytes(hashlib.blake2b(data, digest_size=8).digest(), 'big')
    toks = {t for t in tokens if t}
    if k <= 0:
        raise ValueError('k must be positive')
    if not toks:
        return [MAX64] * k
    return [min(_hash(i, t) for t in toks) for i in range(k)]

def minhash_similarity(signature_a: List[int], signature_b: List[int]) -> float:
    if len(signature_a) != len(signature_b):
        raise ValueError('signatures must have equal length')
    if not signature_a:
        raise ValueError('signatures must not be empty')
    return sum(1 for a, b in zip(signature_a, signature_b) if a == b) / len(signature_a)

def reconstruction_risk_score(unique_quasi_identifiers: int, total_records: int) -> float:
    if total_records == 0:
        return 0.0
    return unique_quasi_identifiers / total_records

