def hybrid_recovery_score(morphology_a: Morphology, morphology_b: Morphology, 
                          log_message: str, recovery_priority_a: float, recovery_priority_b: float, 
                          alpha: float, beta: float) -> float:
    similarity = similarity_score(morphology_a, morphology_b)
    token_frequencies = extract_token_frequencies(log_message)
    shannon_ent = shannon_entropy(token_frequencies)
    recovery_priority = (recovery_priority_a + recovery_priority_b) / 2
    return (alpha * similarity + (1 - alpha) * recovery_priority) * (1 - beta * shannon_ent)

def guide_model_pooling(log_message: str, model_tiers: List[str], 
                       unique_quasi_identifiers: int, total_records: int) -> str:
    token_frequencies = extract_token_frequencies(log_message)
    minhash_sig = minhash_signature(list(token_frequencies.keys()))
    reconstruction_risk = reconstruction_risk_score(unique_quasi_identifiers, total_records)
    # Select model tier based on MinHash similarity and reconstruction risk
    best_tier = min(model_tiers, key=lambda tier: abs(minhash_similarity(minhash_sig, minhash_signature([tier])) - reconstruction_risk))
    return best_tier

