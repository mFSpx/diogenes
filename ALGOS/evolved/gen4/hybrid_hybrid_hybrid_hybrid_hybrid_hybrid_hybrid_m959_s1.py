def sphericity_index(length: float, width: float, height: float) -> float:
    return (math.pi ** (1/3)) * (length * width * height) ** (1/3) / ((length ** 2 + width ** 2 + height ** 2) ** (1/2))

def similarity_score(morphology_a: Morphology, morphology_b: Morphology) -> float:
    vector_a = np.array([morphology_a.length, morphology_a.width, morphology_a.height, morphology_a.mass])
    vector_b = np.array([morphology_b.length, morphology_b.width, morphology_b.height, morphology_b.mass])
    covariance_matrix = np.cov(vector_a, vector_b)
    correlation_coefficient = covariance_matrix[0, 1] / (np.std(vector_a) * np.std(vector_b))
    return correlation_coefficient

def extract_token_frequencies(log_message: str) -> Dict[str, int]:
    tokens = re.findall(r'\b\w+\b', log_message)
    token_frequencies = {}
    for token in tokens:
        if token in token_frequencies:
            token_frequencies[token] += 1
        else:
            token_frequencies[token] = 1
    return token_frequencies

def shannon_entropy(token_frequencies: Dict[str, int]) -> float:
    total_tokens = sum(token_frequencies.values())
    probabilities = [frequency / total_tokens for frequency in token_frequencies.values()]
    return -sum(prob * math.log2(prob) for prob in probabilities)

