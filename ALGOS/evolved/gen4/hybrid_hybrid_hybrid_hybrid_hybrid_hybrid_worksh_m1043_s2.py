def _seed_from_text(text: str) -> int:
    """Create a deterministic integer seed from *text* without external libs."""
    # Simple rolling hash – stable across interpreter runs
    h = 0
    for ch in text:
        h = (h * 31 + ord(ch)) & 0xFFFFFFFFFFFFFFFF
    return h


def extract_features(text: str) -> np.ndarray:
    """
    Generate a FEATURE_DIM‑dimensional feature vector from *text*.
    The same *text* always yields the same vector.
    """
    rnd = random.Random(_seed_from_text(text))
    return np.array([rnd.random() * 10.0 for _ in range(FEATURE_DIM)], dtype=np.float64)


# ----------------------------------------------------------------------
# Entropy utilities (from Parent A)
# ----------------------------------------------------------------------
def shannon_entropy(probs: np.ndarray, eps: float = 1e-12) -> float:
    """Return -∑ p·log(p) for a probability vector."""
    probs = probs / probs.sum()
    probs = np.clip(probs, eps, 1.0)
    return -float(np.sum(probs * np.log(probs)))


