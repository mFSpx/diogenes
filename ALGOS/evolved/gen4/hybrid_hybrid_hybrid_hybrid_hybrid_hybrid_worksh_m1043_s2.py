GROUPS = ("codex", "groq", "cohere", "local_models")
FEATURE_DIM = 12                     # dimensionality of deterministic feature vector
ALPHA = 0.05                         # learning rate for reward‑entropy coupling
BETA = 0.10                          # learning rate for weekday modulation
BASE_HALF_LIFE = 300.0               # seconds, baseline half‑life for pheromones


# ----------------------------------------------------------------------
# Deterministic pseudo‑random feature extraction (from Parent B)
