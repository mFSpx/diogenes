def allocation_scores(
    pheromone_val: float,
    features: np.ndarray,
    weight_matrix: np.ndarray,
) -> np.ndarray:
    """
    Compute raw allocation scores for each GROUP.

    Parameters
    ----------
    pheromone_val : float
        Current pheromone signal for the considered surface.
    features : np.ndarray, shape (FEATURE_DIM,)
        Deterministic feature vector extracted from the request text.
    weight_matrix : np.ndarray, shape (len(GROUPS), FEATURE_DIM)
        Fixed linear mapping from features to group scores.

    Returns
    -------
    scores : np.ndarray, shape (len(GROUPS),)
        Unnormalised scores before soft‑max.
    """
    # pheromone bias acts as a uniform additive term across all groups
    bias = pheromone_val * np.ones(len(GROUPS), dtype=np.float64)
    linear_part = weight_matrix @ features          # shape (len(GROUPS),)
    return bias + linear_part


def softmax(logits: np.ndarray) -> np.ndarray:
    """Numerically stable soft‑max."""
    max_logit = np.max(logits)
    exps = np.exp(logits - max_logit)
    return exps / exps.sum()


def doomsday(year: int, month: int, day: int) -> int:
    """
    Return the doomsday index for a Gregorian date:
    0 = Sunday, …, 6 = Saturday.
    """
    return (date(year, month, day).weekday() + 1) % 7


# ----------------------------------------------------------------------
# Hybrid system class
# ----------------------------------------------------------------------
class HybridPheromoneFeatureAllocator:
    """
    Stores pheromone signals per surface and a static weight matrix.
    Provides allocation, entropy evaluation and pheromone update that
    intertwine the two parent algorithms.
    """

    def __init__(self):
        # surface_key → dict with 'value', 'half_life', 'timestamp'
        self.pheromones = {}
        # Fixed random weight matrix, reproducible across runs
        rnd = random.Random(42)
        self.W = np.array(
            [[rnd.random() * 0.2 for _ in range(FEATURE_DIM)] for _ in GROUPS],
            dtype=np.float64,
        )

    # ------------------------------------------------------------------
    # Pheromone handling (Parent A core)
    # ------------------------------------------------------------------
    def _current_pheromone(self, surface_key: str) -> float:
        """Retrieve the decayed pheromone value for *surface_key*."""
        entry = self.pheromones.get(surface_key)
        if entry is None:
            # initialise with a neutral value
            now = datetime.now(timezone.utc)
            self.pheromones[surface_key] = {
                "value": 0.0,
                "half_life": BASE_HALF_LIFE,
                "timestamp": now,
            }
            return 0.0

        now = datetime.now(timezone.utc)
        elapsed = (now - entry["timestamp"]).total_seconds()
        decay_factor = math.pow(0.5, elapsed / entry["half_life"])
        decayed = entry["value"] * decay_factor
        # update stored value to the decayed one (lazy decay)
        entry["value"] = decayed
        entry["timestamp"] = now
        return decayed

    def update_pheromone(
        self,
        surface_key: str,
        reward: float,
        request_date: date,
    ) -> None:
        """
        Adjust the pheromone's half‑life based on reward, entropy of the last
        allocation and the weekday (doomsday).  The pheromone value itself is
        increased by the reward (positive reinforcement).
        """
        # fetch current decayed value
        current_val = self._current_pheromone(surface_key)

        # compute weekday factor
        weekday = doomsday(request_date.year, request_date.month, request_date.day)

        # Retrieve last allocation probabilities if they exist; otherwise use uniform
        probs = np.full(len(GROUPS), 1.0 / len(GROUPS), dtype=np.float64)
        # (In a full implementation we would store the last probs; here we approximate.)

        entropy = shannon_entropy(probs)

        # Update half‑life using the mathematical bridge
        entry = self.pheromones[surface_key]
        old_tau = entry["half_life"]
        new_tau = old_tau * (1.0 + ALPHA * (reward - entropy)) * (1.0 + BETA * weekday / 7.0)
        entry["half_life"] = max(1.0, new_tau)  # enforce a positive half‑life

        # Reinforce pheromone value with the observed reward
        entry["value"] = current_val + reward
        entry["timestamp"] = datetime.now(timezone.utc)

    # ------------------------------------------------------------------
    # Allocation pipeline (fusion of both parents)
    # ------------------------------------------------------------------
    def allocate(self, surface_key: str, request_text: str, request_date: date) -> dict:
        """
        Perform a full allocation cycle:
        1. Extract deterministic features from *request_text*.
        2. Retrieve the current pheromone value for *surface_key*.
        3. Compute scores via the hybrid linear‑bias formula.
        4. Convert scores to probabilities with soft‑max.
        5. Return a mapping GROUP → probability.
        """
        feats = extract_features(request_text)                         # (d,)
        pher_val = self._current_pheromone(surface_key)               # scalar
        scores = allocation_scores(pher_val, feats, self.W)           # (g,)
        probs = softmax(scores)                                        # (g,)

        # Store the probs for later entropy‑based update (simple cache)
        self.pheromones.setdefault(surface_key, {})["last_probs"] = probs.copy()

        return {group: float(p) for group, p in zip(GROUPS, probs)}

    # ------------------------------------------------------------------
    # Utility to fetch last entropy (used by external callers if desired)
    # ------------------------------------------------------------------
    def last_entropy(self, surface_key: str) -> float:
        entry = self.pheromones.get(surface_key, {})
        probs = entry.get("last_probs")
        if probs is None:
            return math.log(len(GROUPS))  # entropy of uniform distribution
        return shannon_entropy(probs)


