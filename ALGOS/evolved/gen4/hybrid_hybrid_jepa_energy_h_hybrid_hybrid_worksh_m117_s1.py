@dataclass(frozen=True, slots=True)
class ModelTier:
    name: str
    ram_mb: int
    tier: str  
    features: Dict[str, float]
    master_vector: Dict[str, float]

class ModelPool:
    def __init__(self, ram_ceiling_mb: int = 6000):
        self.ram_ceiling_mb: int = ram_ceiling_mb
        self.loaded: Dict[str, ModelTier] = {}
        self._vfe: float = 0.0  

    def _used_ram(self) -> int:
        return sum(m.ram_mb for m in self.loaded.values())

    def _vfe_penalty(self, delta: float) -> None:
        self._vfe += delta

    def is_loaded(self, name: str) -> bool:
        return name in self.loaded

    def free_energy(self) -> float:
        return self._vfe

    def add_model(self, model: ModelTier) -> None:
        if model.tier == "T3" and any(m.tier == "T2" for m in self.loaded.values()):
            self._vfe_penalty(1e10)  
        if model.ram_mb + self._used_ram() > self.ram_ceiling_mb:
            self._vfe_penalty(1e8)  
        self.loaded[model.name] = model

    def compute_vfe(self) -> None:
        self._vfe = 0.0
        for model in self.loaded.values():
            features = model.features
            master_vector = model.master_vector
            vfe = 0.0
            for feature, value in features.items():
                vfe += value * master_vector.get(feature, 0.0)
            self._vfe += vfe

