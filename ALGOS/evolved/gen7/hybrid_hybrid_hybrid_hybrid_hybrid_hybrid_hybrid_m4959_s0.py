def lazy_rw_distribution(adj, node, alpha=0.5):
    neighbours = adj.get(node, [])
    deg = len(neighbours)
    dist = {node: alpha}
    if deg > 0:
        spread = (1.0 - alpha) / deg
        for nb in neighbours:
            dist[nb] = dist.get(nb, 0.0) + spread
    return dist

def bfs_distances(adj):
    distances = {node: {node: 0 for node in adj} for node in adj}
    for start in adj:
        queue = deque([(start, 0)])
        visited = set()
        while queue:
            node, dist = queue.popleft()
            if node not in visited:
                visited.add(node)
                for neighbour in adj[node]:
                    if neighbour not in visited:
                        queue.append((neighbour, dist + 1))
                        distances[start][neighbour] = dist + 1
    return distances

def ollivier_ricci_curvature(adj, node):
    neighbours = adj[node]
    deg = len(neighbours)
    curvature = 0
    for nb in neighbours:
        curvature += 1 - (1 / deg) * sum(1 for nbb in adj[nb] if nbb in neighbours)
    return curvature / deg

