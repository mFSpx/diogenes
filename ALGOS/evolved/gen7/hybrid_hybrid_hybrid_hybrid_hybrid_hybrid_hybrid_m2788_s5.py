def lead_lag_bspline_signature(series: np.ndarray, knots: int = 5) -> np.ndarray:
    """
    Approximate a B‑spline signature of a 1‑D time series.
    The implementation uses a simple moving‑average basis as a stand‑in
    for real B‑spline evaluation.

    Parameters
    ----------
    series : np.ndarray
        Input time‑series (shape: (T,)).
    knots : int
        Number of basis functions (default 5).

    Returns
    -------
    signature : np.ndarray
        Vector of length `knots` containing the aggregated basis responses.
    """
    T = series.shape[0]
    if T < knots:
        raise ValueError("Series length must exceed number of knots.")
    # Uniform knot positions
    positions = np.linspace(0, T - 1, knots, dtype=int)
    # Simple triangular basis around each knot
    sig = np.zeros(knots)
    width = max(1, T // (2 * knots))
    for i, pos in enumerate(positions):
        left = max(0, pos - width)
        right = min(T, pos + width + 1)
        weights = np.linspace(0, 1, right - left)
        sig[i] = np.dot(series[left:right], weights) / (right - left + 1e-9)
    # Normalise to unit L2 norm
    norm = np.linalg.norm(sig) + 1e-12
    return sig / norm

