    f = 1.0 / (1.0 + np.exp(-concat.mean()))  # scalar between 0 and 1

    # 3. Differential update (Euler step, dt=1)
    dx = -(1.0 / tau_prime + f) * x + f * A
    x_next = x + dx

    # 4. Diffusion noise injection
    T = len(alpha_schedule)
