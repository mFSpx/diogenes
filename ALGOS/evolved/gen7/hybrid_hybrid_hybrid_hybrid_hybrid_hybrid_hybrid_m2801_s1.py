    def update(self, action: BanditAction, burst_signals: List[BurstSignal]) -> None:
        delta_store = self.alpha * action.propensity - self.beta * action.confidence_bound
        self.store = max(0, self.store + delta_store)

        aggregated_burst_signal = np.mean([signal.z_score for signal in burst_signals])
        self.eta = self.eta0 * (1 + aggregated_burst_signal)

