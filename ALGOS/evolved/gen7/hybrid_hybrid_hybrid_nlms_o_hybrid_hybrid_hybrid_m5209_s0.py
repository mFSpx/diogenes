def gaussian_beam(theta: float, center: float, width: float) -> float:
    if width <= 0:
        raise ValueError('width must be positive')
    z = (theta - center) / width
    return math.exp(-0.5 * z * z)

def fisher_score(theta: float, center: float, width: float, eps: float = 1e-12) -> float:
    intensity = max(gaussian_beam(theta, center, width), eps)
    derivative = intensity * (-(theta - center) / (width * width))
    return (derivative * derivative) / intensity

class HybridNLMS:
    def __init__(self, weights: np.ndarray, mu: float = 0.5, eps: float = 1e-9):
        self.weights = weights
        self.mu = mu
        self.eps = eps
        self.center = np.mean(self.weights)
        self.width = np.std(self.weights)

    def predict(self, x: np.ndarray) -> float:
        return np.dot(self.weights, x)

    def update(self, x: np.ndarray, target: float) -> tuple[np.ndarray, float]:
        y = self.predict(x)
        error = target - y
        power = np.dot(x, x) + self.eps
        next_weights = self.weights + self.mu * error * x / power
        self.center = np.mean(next_weights)
        self.width = np.std(next_weights)
        return next_weights, error

    def hybrid_update(self, x: np.ndarray, target: float) -> tuple[np.ndarray, float]:
        similarity = gaussian_beam(target, self.center, self.width)
        fisher_value = fisher_score(target, self.center, self.width)
        next_weights, error = self.update(x, target)
        self.weights = next_weights
        return next_weights, error * similarity * fisher_value

