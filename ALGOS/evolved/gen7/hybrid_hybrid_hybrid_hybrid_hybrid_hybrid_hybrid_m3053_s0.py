    def gini_coefficient(self, feature_values: np.ndarray) -> float:
        # Calculate the Gini coefficient
        gini = 1.0
        for value in np.unique(feature_values):
            p = np.sum(feature_values == value) / len(feature_values)
            gini -= p ** 2
        return gini

    def shannon_entropy(self, feature_values: np.ndarray) -> float:
        # Calculate the Shannon entropy
        entropy = 0.0
