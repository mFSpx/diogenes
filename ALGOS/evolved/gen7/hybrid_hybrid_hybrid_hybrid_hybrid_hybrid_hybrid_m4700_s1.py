def kullback_leibler_divergence(p, q):
    return np.sum(np.where(p != 0, p * np.log(p / q), 0))

def compute_expected_rewards(tropical_network, voronoi_regions):
    tropical_outputs = tropical_network.evaluate(voronoi_regions)
    voronoi_probabilities = np.array([region[1] for region in voronoi_regions])
    kl_divergences = np.array([kullback_leibler_divergence(tropical_output, voronoi_probability) 
                               for tropical_output, voronoi_probability in zip(tropical_outputs, voronoi_probabilities)])
    expected_rewards = 1 / (1 + np.exp(-kl_divergences))
    return expected_rewards

