def hyperloglog(items, p: int = 14):
    """Return the HyperLogLog cardinality estimate (E) and the raw register sum (V)."""
    m = 1 << p
    M = [0] * m
    for item in items:
        x = int(hashlib.sha256(str(item).encode()).hexdigest(), 16)
        j = x >> (64 - p)                     # bucket index
        w = x & ((1 << (64 - p)) - 1)         # remaining bits
        rho = (w.bit_length() - (64 - p)) + 1  # position of first 1
        M[j] = max(M[j], rho)
