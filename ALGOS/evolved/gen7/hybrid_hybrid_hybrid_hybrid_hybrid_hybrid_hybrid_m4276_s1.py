def gaussian_rbf(r: float, epsilon: float = 1.0) -> float:
    """Gaussian radial basis function."""
    return math.exp(-((epsilon * r) ** 2))


def euclidean(a: np.ndarray, b: np.ndarray) -> float:
    """Euclidean distance between two 1‑D arrays."""
    if a.shape != b.shape:
        raise ValueError("vectors must have same dimension")
    return np.sqrt(np.sum((a - b) ** 2))


def solve_linear(a: np.ndarray, b: np.ndarray) -> np.ndarray:
    """
    Solve the linear system A x = b using Gaussian elimination with partial pivoting.
    a: (n, n) matrix, b: (n,) vector.
    Returns x as an (n,) ndarray.
    """
    n = b.shape[0]
    # Build augmented matrix
    aug = np.hstack([a.astype(float), b.reshape(-1, 1)])
    for col in range(n):
        # Pivot
        pivot_row = np.argmax(np.abs(aug[col:, col])) + col
        if abs(aug[pivot_row, col]) < 1e-12:
            raise ValueError("singular system")
        # Swap rows
        if pivot_row != col:
            aug[[col, pivot_row]] = aug[[pivot_row, col]]
        # Normalize pivot row
        aug[col] = aug[col] / aug[col, col]
        # Eliminate other rows
        for row in range(n):
            if row != col:
                aug[row] -= aug[row, col] * aug[col]
    return aug[:, -1]


def rbf_surrogate_predict(x: np.ndarray, centers: np.ndarray, weights: np.ndarray, epsilon: float = 1.0):
    """
    Compute the RBF surrogate output:
        f(x) = Σ_i w_i * φ(||x - c_i||)
    where φ is the Gaussian RBF.
    """
    if centers.shape[0] != weights.shape[0]:
        raise ValueError("number of centers must match number of weights")
    vals = np.array([gaussian_rbf(euclidean(x, c), epsilon) for c in centers])
    return np.dot(weights, vals)


