def sketch_to_feature_vector(sketch_table):
    """
    Convert a Count‑Min sketch (list of lists) into a fixed‑size feature vector.
    The conversion concatenates the rows and normalises by the total count.
    """
    flat = np.array(sketch_table).flatten().astype(float)
    total = flat.sum()
    if total == 0:
        return flat
    return flat / total


def hybrid_score_matrix(sketch_vec: np.ndarray, configs, centers, weights, epsilon=1.0):
    """
    Build a score matrix where each row corresponds to a routing configuration
    and each column corresponds to a surrogate component (here we use a single
    scalar per configuration).  The matrix is semiseparable in the sense that
    it can be expressed as U * V^T with U derived from config vectors and V
    derived from the RBF surrogate evaluated on the sketch vector.
    """
    # Encode each configuration as a binary (one‑hot) vector of length depth*width
    # for simplicity we reuse the sketch dimension.
    depth = len(configs[0])
    width = 1  # we treat each routing decision as a scalar feature
    encoded = np.zeros((len(configs), depth * width))
    for i, cfg in enumerate(configs):
        encoded[i, :depth] = cfg  # pad/truncate to depth
    # Compute surrogate scores for the *same* sketch vector (treated as x)
    surrogate_score = rbf_surrogate_predict(sketch_vec, centers, weights, epsilon)
    # Form a rank‑1 (semiseparable) matrix: U * V^T
    U = encoded
    V = np.full((1, depth * width), surrogate_score)
    return U @ V.T  # shape (n_configs, n_configs)


