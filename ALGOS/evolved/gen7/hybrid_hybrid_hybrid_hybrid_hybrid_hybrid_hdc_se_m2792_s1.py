def bind(a: list[int], b: list[int]) -> list[int]:
    if len(a) != len(b):
        raise ValueError("vectors must have equal length")
    return [x * y for x, y in zip(a, b)]

def bundle(vectors: list[list[int]]) -> list[int]:
    vecs = list(vectors)
    return [sum(x) for x in zip(*vecs)]

