def haversine(a: tuple[float, float], b: tuple[float, float]) -> float:
    lat1, lon1 = map(math.radians, a)
    lat2, lon2 = map(math.radians, b)
    dlat = lat2 - lat1
    dlon = lon2 - lon1
    h = math.sin(dlat / 2) ** 2 + math.cos(lat1) * math.cos(lat2) * math.sin(dlon / 2) ** 2
    return 2 * 6_371_000.0 * math.asin(min(1.0, math.sqrt(h)))


def gini_coefficient(values: list[float]) -> float:
    if not values:
        return 0.0
    xs = sorted(values)
    if sum(xs) == 0.0:
        return 0.0
    if xs[0] < 0:
        raise ValueError("values must be non-negative")
    n = len(xs)
    numerator = sum((2 * i - n - 1) * x for i, x in enumerate(xs, start=1))
    denominator = n * sum(xs)
    return numerator / denominator


def detect_bursts(events: list[dict], key: str = 'category') -> list[BurstSignal]:
    counter = {}
    for event in events:
        category = event[key]
        counter[category] = counter.get(category, 0) + 1
    mean = sum(counter.values()) / len(counter)
    variance = sum((count - mean) ** 2 for count in counter.values()) / len(counter)
    bursts = []
    for k, count in counter.items():
        z_score = (count - mean) / math.sqrt(variance) if variance != 0 else 0
        bursts.append(BurstSignal(k, count, z_score))
    return bursts


