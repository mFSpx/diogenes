def calculate_gini(burst_signals: List[BurstSignal]) -> float:
    scores = [signal.count for signal in burst_signals]
    mean_score = np.mean(scores)
    variance = np.var(scores)
    gini = 1 - (2 * np.sum([(i + 1) * x for i, x in enumerate(np.sort(scores))]) / (len(scores) * np.sum(scores)))
    return gini

