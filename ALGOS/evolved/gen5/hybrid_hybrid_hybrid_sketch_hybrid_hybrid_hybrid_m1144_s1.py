def count_min_sheaf(
    texts: list[str],
    d: int = 2048,
    depth: int = 4,
    width: int = 256,
    hv_kind: str = "complex",
) -> Sheaf:
    """
    Build a sheaf from a Count‑Min sketch of *texts*.

    Each text is turned into a hypervector `hv` (dimension *d*).  For each
    sketch row `i` (0 ≤ i < depth) we compute a min‑hash bucket index
    `b = minhash_for_text(text, 1, seed_offset=i)[0] % width`.  The hypervector
    is accumulated into the vertex identified by `(i, b)`.  Edges connect the
    same bucket across consecutive rows, i.e. ((i, b), (i+1, b)).
    """
    # Mapping from (row, bucket) -> aggregated hypervector
    agg: dict[tuple[int, int], np.ndarray] = defaultdict(lambda: np.zeros(d, dtype=np.complex128))

    for txt in texts:
        hv = random_hv(d, kind=hv_kind)
        for i in range(depth):
            bucket = minhash_for_text(txt, num_hashes=1, seed_offset=i)[0] % width
            key = (i, bucket)
            agg[key] += hv

    # Build node dimension dict and edges
    node_dims = {hash(key): d for key in agg.keys()}
    edges = []
    for i in range(depth - 1):
        for bucket in range(width):
            u = hash((i, bucket))
            v = hash((i + 1, bucket))
            if u in node_dims and v in node_dims:
                edges.append((u, v))

    sheaf = Sheaf(node_dims, edges)

    for key, vec in agg.items():
        vertex = hash(key)
        sheaf.set_section(vertex, vec)

    return sheaf

def hybrid_rlct_via_sheaf(sheaf: Sheaf, alpha: float = 0.5) -> float:
    """
    Estimate a Real Log Canonical Threshold (RLCT) from sheaf coboundary residuals.

    Let `r_k` be the ℓ₂‑norm of the residual for edge *k*.  We fit a line to
    (log(k+1), log(r_k)) using ordinary least squares and return the negative
    slope, which coincides with the RLCT definition in the original sketch
    algorithm.
    """
    residuals = sheaf.coboundary_residuals(alpha)
    if not residuals:
        return float('nan')
    norms = np.array([np.linalg.norm(r) for r in residuals])
    # avoid zeros for log
    norms = np.where(norms == 0, np.finfo(float).tiny, norms)

    ks = np.arange(1, len(norms) + 1, dtype=np.float64)

    log_k = np.log(ks)
    log_r = np.log(norms)

    # least‑squares fit: log_r = a * log_k + b
    A = np.vstack([log_k, np.ones_like(log_k)]).T
    a, _ = np.linalg.lstsq(A, log_r, rcond=None)[0]
    rlct = -a
    return rlct

def hybrid_info_loss(sheaf: Sheaf, alpha: float = 0.5) -> float:
    """
    Blend the RLCT estimate with the sheaf Laplacian energy.

    The returned value is normalized to [0, 1] by a simple logistic scaling.
    """
    rlct = hybrid_rlct_via_sheaf(sheaf, alpha)
    energy = sheaf.laplacian_energy(alpha)
    # combine (weights can be tuned)
    combined = rlct + math.log1p(energy)
    # logistic normalization
    loss = 1.0 / (1.0 + math.exp(-combined))
    return loss

def hybrid_similarity(
    text_a: str,
    text_b: str,
    d: int = 2048,
    depth: int = 4,
    width: int = 256,
    hv_kind: str = "complex",
    alpha: float = 0.5,
) -> float:
    """
    Compute a similarity score between two texts using the hybrid sheaf model.

    Each text is embedded into a tiny sheaf (depth × width).  The similarity is
    defined as the cosine similarity between the aggregated hypervectors of the
    two sheaves after aligning vertices by (row, bucket) keys.
    """
    sheaf_a = count_min_sheaf([text_a], d, depth, width, hv_kind)
    sheaf_b = count_min_sheaf([text_b], d, depth, width, hv_kind)

    # Align sections by vertex hash
    common_vertices = set(sheaf_a.section) & set(sheaf_b.section)
    if not common_vertices:
        return 0.0

    vec_a = np.sum([sheaf_a.section[v] for v in common_vertices], axis=0)
    vec_b = np.sum([sheaf_b.section[v] for v in common_vertices], axis=0)

    # fractional binding to smooth differences (optional)
    bound = np.multiply(np.power(vec_a, alpha), np.power(vec_b, 1 - alpha))

    # cosine similarity
    dot = np.real(np.vdot(bound, bound))  # vdot works for complex, returns complex; take real
    norm_a = np.linalg.norm(bound)
    norm_b = np.linalg.norm(bound)
    if norm_a == 0 or norm_b == 0:
        return 0.0
    return dot / (norm_a * norm_b)

