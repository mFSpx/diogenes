class Sheaf:
    """Cellular sheaf whose vertices store hypervectors."""

    def __init__(self, node_dims: dict[int, int], edges: list[tuple[int, int]]):
        self.node_dims = dict(node_dims)          # vertex -> dimension (all equal here)
        self.edges = list(edges)                  # list of (u, v) oriented edges
        self.section: dict[int, np.ndarray] = {}  # vertex -> hypervector (section value)

    def set_section(self, vertex: int, vector: np.ndarray):
        """Assign a hypervector to a vertex (overwrites previous)."""
        expected = self.node_dims[vertex]
        if vector.shape != (expected,):
            raise ValueError(f"vector shape {vector.shape} does not match dimension {expected}")
        self.section[vertex] = vector

    def coboundary_residuals(self, alpha: float = 0.5) -> list[np.ndarray]:
        """
        Compute δ(s) for each edge using fractional‑power binding as the restriction map.
        For an edge (u, v) the residual is  r = s[u] - (s[u]**alpha * s[v]**(1‑alpha)).
        """
        res = []
        for u, v in self.edges:
            su = self.section.get(u)
            sv = self.section.get(v)
            if su is None or sv is None:
                continue
            # fractional binding (element‑wise power, safe for complex vectors)
            bound = np.multiply(np.power(su, alpha), np.power(sv, 1 - alpha))
            residual = su - bound
            res.append(residual)
        return res

    def laplacian_energy(self, alpha: float = 0.5) -> float:
        """Sum of squared ℓ₂‑norms of coboundary residuals."""
        return sum(np.linalg.norm(r) ** 2 for r in self.coboundary_residuals(alpha))

