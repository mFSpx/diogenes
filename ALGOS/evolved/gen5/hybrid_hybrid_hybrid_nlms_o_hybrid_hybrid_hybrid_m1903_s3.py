NodeId = int
EdgeKey = FrozenSet[NodeId]


@dataclass
class Node:
    """Static node data."""
    pos: np.ndarray               # 2‑D position vector
    neighbors: List[NodeId]       # adjacent node identifiers
    impedance: Dict[NodeId, float]  # impedance weight per neighbour


def build_random_graph(num_nodes: int, seed: int = 0) -> Tuple[Dict[NodeId, Node], Dict[EdgeKey, float]]:
    """
    Creates a random undirected graph with symmetric impedances
    and uniform edge priors (0.5).
    """
    random.seed(seed)
    np.random.seed(seed)

    nodes: Dict[NodeId, Node] = {}
    for i in range(num_nodes):
        pos = np.random.rand(2)
        nodes[i] = Node(pos=pos, neighbors=[], impedance={})

    # Random edges (ensure connectivity)
    edges: Dict[EdgeKey, float] = {}
    for i in range(num_nodes):
        for j in range(i + 1, num_nodes):
            if random.random() < 0.3:  # 30 % edge probability
                imp = random.uniform(0.1, 1.0)
                nodes[i].neighbors.append(j)
                nodes[i].impedance[j] = imp
                nodes[j].neighbors.append(i)
                nodes[j].impedance[i] = imp
                edges[frozenset({i, j})] = 0.5  # prior probability

    # Guarantee at least a spanning tree if graph is too sparse
    if not edges:
        for i in range(num_nodes - 1):
            imp = random.uniform(0.1, 1.0)
            nodes[i].neighbors.append(i + 1)
            nodes[i].impedance[i + 1] = imp
            nodes[i + 1].neighbors.append(i)
            nodes[i + 1].impedance[i] = imp
            edges[frozenset({i, i + 1})] = 0.5

    return nodes, edges


