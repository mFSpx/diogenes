def bayes_marginal(prior: float, likelihood: float, false_positive: float) -> float:
    """Compute the marginal probability (8)."""
    return likelihood * prior + false_positive * (1.0 - prior)


def bayes_update(prior: float, likelihood: float, marginal: float) -> float:
    """Compute the posterior probability (9)."""
    return prior * likelihood / marginal


def bayes_update_edge(
    prior: float, likelihood: float, false_positive: float = 0.01
) -> float:
    """
    Full Bayesian update for a single edge.
    ``false_positive`` is a small constant modelling spurious evidence.
    """
    m = bayes_marginal(prior, likelihood, false_positive)
    if m <= 0.0:
        return prior  # safeguard
    return bayes_update(prior, likelihood, m)


