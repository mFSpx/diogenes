def hybrid_predict(
    node_id: NodeId,
    nodes: Dict[NodeId, Node],
    w: np.ndarray,
    theta: np.ndarray,
    t: float,
) -> Tuple[float, np.ndarray]:
    """
    Computes the prediction ŷ_i(t) (5) and returns the KAN feature ψ_i(t).
    """
    node = nodes[node_id]

    # (1) impedance‑weighted neighbourhood vector (2‑D)
    x_g = np.zeros(2)
    for nbr in node.neighbors:
        imp = node.impedance[nbr]
        phi_j = nodes[nbr].pos
        x_g += imp * phi_j

    # Temporal states for rectified flow (2)
    x0 = node.pos
    # A simple forward state: add a small random drift
    x1 = x0 + 0.05 * np.random.randn(2)

    z = t * x1 + (1.0 - t) * x0

    # Concatenated raw vector (3)
    u = np.concatenate([x_g, z])

    # KAN expansion (4)
    psi = kan_transform(u, theta)

    # Linear prediction (5)
    y_hat = float(w @ psi)

    return y_hat, psi


def hybrid_update_graph(
    nodes: Dict[NodeId, Node],
    edges: Dict[EdgeKey, float],
    observations: Dict[NodeId, float],
    w: np.ndarray,
    theta: np.ndarray,
    mu: float = 0.1,
    eps: float = 1e-6,
    t: float = 0.5,
    false_positive: float = 0.01,
) -> Tuple[np.ndarray, Dict[EdgeKey, float]]:
    """
    Executes one hybrid learning step:
      * forward pass for every node,
      * NLMS weight adaptation with a node‑wise learning‑rate μ_i
        scaled by the average posterior of incident edges,
      * Bayesian edge posterior update using a likelihood derived from
        the absolute prediction error.
    Returns the updated weight vector and edge posterior dictionary.
    """
    w_curr = w.copy()
    for nid, target in observations.items():
        # Forward prediction
        y_hat, psi = hybrid_predict(nid, nodes, w_curr, theta, t)

        # Compute average posterior of incident edges
        incident_edges = [
            frozenset({nid, nbr}) for nbr in nodes[nid].neighbors
        ]
        if incident_edges:
            avg_post = np.mean([edges[ek] for ek in incident_edges])
        else:
            avg_post = 0.5  # fallback

        # Modulated learning rate (10)
        mu_i = mu * avg_post

        # NLMS update with μ_i
        w_curr, e = nlms_update(w_curr, psi, target, mu=mu_i, eps=eps)

        # Likelihood from error – larger error ⇒ smaller likelihood
        likelihood = math.exp(- (e ** 2))

        # Update posteriors of incident edges
        for ek in incident_edges:
            prior = edges[ek]
            post = bayes_update_edge(prior, likelihood, false_positive)
            edges[ek] = post

    return w_curr, edges


