def kan_transform(u: np.ndarray, theta: np.ndarray) -> np.ndarray:
    """
    Simple KAN‑style spline expansion.
    For each component of ``u`` we apply a quadratic B‑spline‑like basis
    and then a learned linear mixing given by ``theta``.
    The implementation uses a tanh non‑linearity as a surrogate.
    """
    # Quadratic “spline” basis (max(0, u)^2)
    spline = np.maximum(0.0, u) ** 2
    # Linear mixing and smooth non‑linearity
    return np.tanh(spline @ theta)


def nlms_update(
    w: np.ndarray,
    psi: np.ndarray,
    target: float,
    mu: float = 0.1,
    eps: float = 1e-6,
) -> Tuple[np.ndarray, float]:
    """
    Normalized LMS weight adaptation.
    Returns the updated weight vector and the instantaneous error.
    """
    pred = float(w @ psi)
    e = target - pred
    norm_sq = float(np.linalg.norm(psi) ** 2)
    w_new = w + mu * e * psi / (norm_sq + eps)
    return w_new, e


