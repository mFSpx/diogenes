def chunk_text_tokens(text: str, max_tokens: int = 200, overlap_tokens: int = 0) -> List[TextChunk]:
    tokens = tokenize(text)
    chunks = []
    for i in range(0, len(tokens), max_tokens - overlap_tokens):
        chunk_id = f"chunk:{i}"
        chunk_index = i
