def ssim(x: np.ndarray, y: np.ndarray,
         dynamic_range: float = DYNAMIC_RANGE,
         k1: float = K1, k2: float = K2) -> float:
    """
    One‑dimensional SSIM implementation (identical to the parent’s formula).

    Parameters
    ----------
    x, y : np.ndarray
        1‑D arrays of equal length.
    dynamic_range : float
        Maximum possible value of the input (default 255 for 8‑bit images).
    k1, k2 : float
        Stability constants.

    Returns
    -------
    float
        SSIM index in [0, 1].
    """
    if x.shape != y.shape:
        raise ValueError("x and y must have the same shape")
    mean_x = np.mean(x)
    mean_y = np.mean(y)
    cov_xy = np.mean((x - mean_x) * (y - mean_y))
    cov_xx = np.mean((x - mean_x) ** 2)
    cov_yy = np.mean((y - mean_y) ** 2)

    c1 = (k1 * dynamic_range) ** 2
    c2 = (k2 * dynamic_range) ** 2

    numerator = (2 * mean_x * mean_y + c1) * (2 * cov_xy + c2)
    denominator = (mean_x ** 2 + mean_y ** 2 + c1) * (cov_xx + cov_yy + c2)

    return float(numerator / denominator) if denominator != 0 else 0.0


