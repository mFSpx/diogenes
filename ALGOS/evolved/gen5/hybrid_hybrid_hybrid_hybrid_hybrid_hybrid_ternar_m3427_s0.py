def hybrid_allocate(dates: List[datetime],
                    multivector: np.ndarray,
                    ltc_params: np.ndarray,
                    bandit_weights: np.ndarray) -> Tuple[np.ndarray, np.ndarray]:
    """
    Compute per‑day, per‑group allocations while updating the bandit.

    The algorithm proceeds day‑by‑day:
    1. Compute the geometric product for the current day‑of‑week slice.
    2. Collapse to a per‑group feature vector.
    3. Compare to the previous day’s feature vector via SSIM (reward).
    4. Update bandit weights with the reward and re‑scale LTC parameters.
    5. Produce the allocation for the day as a weighted sum.

    Parameters
    ----------
    dates : List[datetime]
        Chronological list of dates.
    multivector, ltc_params : np.ndarray (dim, num_groups)
        Current model parameters.
    bandit_weights : np.ndarray (num_groups,)
        Current bandit probability distribution.

    Returns
    -------
    allocations : np.ndarray (len(dates), num_groups)
        Allocation matrix.
    updated_bandit : np.ndarray (num_groups,)
        Bandit weights after processing all dates.
    """
    num_days = len(dates)
    num_groups = multivector.shape[1]
    allocations = np.zeros((num_days, num_groups))
    prev_feature = None

    for idx, dt in enumerate(dates):
        dow = dt.weekday()                     # 0 = Monday, …, 6 = Sunday
        # 1️⃣ Geometric product for the slice of the current day
        gp_slice = geometric_product(multivector[dow:dow+1, :],
                                     ltc_params[dow:dow+1, :])   # shape (1, num_groups)
        feature_vec = gp_slice.squeeze()      # shape (num_groups,)

        # 2️⃣ Similarity reward (SSIM) against previous day's feature vector
        if prev_feature is None:
            reward = 1.0                      # No history → maximal reward
        else:
            reward = ssim(prev_feature, feature_vec)
        prev_feature = feature_vec.copy()

        # 3️⃣ Bandit update and LTC re‑scaling
        bandit_weights = bandit_update(bandit_weights, reward)
        # Scale LTC parameters for this day by the new bandit distribution
        ltc_params[dow, :] = ltc_params[dow, :] * bandit_weights

        # 4️⃣ Allocation: weighted sum of the updated product slice
        allocations[idx, :] = feature_vec * bandit_weights

    return allocations, bandit_weights


