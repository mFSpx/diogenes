def bandit_update(weights: np.ndarray, reward: float, gamma: float = BANDIT_GAMMA) -> np.ndarray:
    """
    Multiplicative‑weights update for a single‑arm reward.

    The same reward is applied to all arms; in practice one could supply a
    vector of rewards per arm. Here we keep the formulation simple.

    Parameters
    ----------
    weights : np.ndarray
        Current bandit probability distribution (shape: (num_groups,)).
    reward : float
        Scalar reward in [0, 1] (SSIM similarity).
    gamma : float
        Learning rate.

    Returns
    -------
    np.ndarray
        Updated and renormalised weight vector.
    """
    # Exponential weighting
    updated = weights * np.exp(gamma * reward)
    # Normalise to a probability distribution
    total = np.sum(updated)
    if total == 0:
        # Avoid division by zero – revert to uniform distribution
        return np.full_like(weights, 1.0 / weights.size)
    return updated / total


