def kullback_leibler_divergence(p: np.ndarray, q: np.ndarray) -> float:
    """
    Compute the Kullback-Leibler divergence between two probability distributions.

    Args:
    p (np.ndarray): The first probability distribution.
    q (np.ndarray): The second probability distribution.

    Returns:
    float: The Kullback-Leibler divergence between p and q.
    """
    epsilon = 1e-15
    p = np.clip(p, epsilon, 1 - epsilon)
    q = np.clip(q, epsilon, 1 - epsilon)
    return np.sum(p * np.log(p / q))

