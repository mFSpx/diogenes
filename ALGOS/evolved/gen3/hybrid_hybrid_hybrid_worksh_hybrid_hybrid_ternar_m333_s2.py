# MinHash utilities (Parent A)
# ----------------------------------------------------------------------
def _hash(seed: int, token: str) -> int:
    """Simple 64‑bit hash based on Python's built‑in hash combined with a seed."""
    random.seed(seed)
    # Mix the token with the seed; the result is forced into 64‑bit space.
    mixed = hash((seed, token)) & MAX64
    return mixed


def minhash_signature(token_set: Set[str], k: int = MINHASH_K) -> List[int]:
    """
    Compute a MinHash signature of length ``k`` for ``token_set``.
    Each entry is the minimum hash value observed for a distinct random seed.
    """
    signature = []
    for i in range(k):
        min_val = min(_hash(i, token) for token in token_set)
        signature.append(min_val)
    return signature


def minhash_similarity(sig_a: List[int], sig_b: List[int]) -> float:
    """
    Estimate Jaccard similarity from two MinHash signatures.
    """
    if len(sig_a) != len(sig_b):
        raise ValueError("Signatures must have equal length")
    matches = sum(1 for a, b in zip(sig_a, sig_b) if a == b)
    return matches / len(sig_a)


# ----------------------------------------------------------------------
# Variational Free‑Energy utilities (Parent B)
# ----------------------------------------------------------------------
def gaussian_kl(mean_q: np.ndarray, var_q: np.ndarray,
                mean_p: np.ndarray, var_p: np.ndarray) -> np.ndarray:
    """
    KL divergence KL(q‖p) for diagonal‑covariance Gaussians.
    Returns a vector of per‑dimension KL terms.
    """
    return 0.5 * (
        np.log(var_p / var_q) +
        (var_q + (mean_q - mean_p) ** 2) / var_p -
        1.0
    )


def variational_free_energy(state_mean: np.ndarray,
                            state_var: np.ndarray,
                            prior_mean: np.ndarray,
                            prior_var: np.ndarray,
                            weight_vec: np.ndarray) -> float:
    """
    Compute the scalar free‑energy:
        F = E_q[log p(x|z)] – λ * (w·KL(q‖p))

    For this demo ``E_q[log p(x|z)]`` is approximated by a simple negative L2 norm
    of the state (as a proxy for reconstruction log‑likelihood).
    """
    # Proxy reconstruction term (negative squared L2 norm)
    recon = -np.sum(state_mean ** 2)

    # Weighted KL term
    kl_vec = gaussian_kl(state_mean, state_var, prior_mean, prior_var)
    weighted_kl = np.dot(weight_vec, kl_vec)

    return recon - LAMBDA * weighted_kl


# ----------------------------------------------------------------------
# Hybrid gating – combines weekday weights and MinHash similarity (core of Parent A)
# ----------------------------------------------------------------------
def gating_factor(weight_vec: np.ndarray, similarity: float) -> float:
    """
    Compute the LTC gating scalar g = σ(α * w·s⃗).

    The similarity scalar is broadcast across the weight vector to form s⃗.
    """
    s_vec = np.full_like(weight_vec, similarity)
    inner = np.dot(weight_vec, s_vec)          # scalar ∈ [0,1]
    g = 1.0 / (1.0 + math.exp(-ALPHA * (inner - 0.5)))  # sigmoid centred at 0.5
    return g


def effective_time_constant(base_tau: float, gating: float) -> float:
    """τ = τ₀ / g  (larger gating → smaller effective τ)."""
    # Guard against division by zero
    return base_tau / (gating + 1e-8)


# ----------------------------------------------------------------------
# Dummy ternary router (stand‑in for the external service in Parent B)
# ----------------------------------------------------------------------
def dummy_route_command(text: str, intent: str, context: Dict[str, Any]) -> Dict[str, Any]:
    """
    Very lightweight deterministic router that pretends to produce a distribution
    over three possible actions. The distribution is encoded as a dict with keys
    'a', 'b', 'c' summing to 1.
    """
    random.seed(hash((text, intent)) & MAX64)
    probs = np.random.dirichlet(np.ones(3), size=1).flatten()
    return {
        "action_distribution": {"a": float(probs[0]),
                                "b": float(probs[1]),
                                "c": float(probs[2])},
        "selected_action": ["a", "b", "c"][int(np.argmax(probs))],
        "intent": intent,
        "text": text,
        "context_summary": {k: str(v)[:30] for k, v in context.items()}
    }


