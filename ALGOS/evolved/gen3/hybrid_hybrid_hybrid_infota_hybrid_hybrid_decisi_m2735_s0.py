class HyperLogLog:
    def __init__(self, b: int = 5):
        self.b = b
        self.M = [0] * (1 << b)

    def add(self, item: str):
        x = int(hashlib.sha1(item.encode('utf-8')).hexdigest(), 16)
        j = (x >> (self.b)) & ((1 << self.b) - 1)
        w = x & ((1 << self.b) - 1)
        self.M[j] = max(self.M[j], self._rho(w))

    def _rho(self, w: int) -> int:
        if w == 0:
            return 32
        return 1 + math.floor(math.log2((w ^ (w - 1)) + 1))

    def estimate(self) -> float:
        alpha = 0.7213 / (1 + 1.079 / (1 << self.b))
        sum_M = sum(math.log((1 << self.b) / m) for m in self.M if m > 0)
        return alpha * (1 << self.b) * math.exp(sum_M / (1 << self.b))

