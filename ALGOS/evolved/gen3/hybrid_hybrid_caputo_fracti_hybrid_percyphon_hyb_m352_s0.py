def adaptive_circuit_breaker(morphology, ternary_offset):
    """Adaptive circuit-breaker

    :param morphology: Morphology parameters
    :param ternary_offset: Ternary offset
    :return: Adaptive circuit-breaker threshold
    """
    sphericity_index = (morphology['length'] * morphology['width'] * morphology['height']) ** (1/3) / morphology['mass']
    flatness_index = morphology['length'] / morphology['width']
    base_failure_threshold = 10
    return base_failure_threshold * (1 + sphericity_index * flatness_index) * (1 + 0.1*ternary_offset)


