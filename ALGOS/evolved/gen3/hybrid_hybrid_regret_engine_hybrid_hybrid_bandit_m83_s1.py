def gini_coefficient(values: Iterable[float]) -> float:
    xs = sorted(float(x) for x in values)
    if not xs or sum(xs) == 0: return 0.0
    if xs[0] < 0: raise ValueError("values must be non-negative")
    n = len(xs)
    return sum((2*i-n-1)*x for i,x in enumerate(xs,1))/(n*sum(xs))

def doomsday(year: int, month: int, day: int) -> int:
    return (dt.date(year, month, day).weekday() + 1) % 7

def weekday_distribution(year: int, month: int, num_days: int) -> np.ndarray:
    weekdays = [doomsday(year, month, day) for day in range(1, num_days+1)]
    weekday_counts = np.zeros(7)
    for weekday in weekdays:
        weekday_counts[weekday-1] += 1
    return weekday_counts

def compute_koopman_operator(action_values: np.ndarray) -> np.ndarray:
    n = len(action_values)
    K = np.zeros((n, n))
    for i in range(n):
        for j in range(n):
            K[i, j] = np.dot(action_values[i], action_values[j]) / np.dot(action_values[i], action_values[i])
    return K

def forecast_rewards(action_values: np.ndarray, K: np.ndarray, steps: int) -> np.ndarray:
    forecasted_values = action_values
    for _ in range(steps):
        forecasted_values = np.dot(K, forecasted_values)
    return forecasted_values

def hybrid_select_action(actions: list[MathAction], counterfactuals: list[MathCounterfactual], 
                         action_values: np.ndarray, K: np.ndarray) -> BanditAction:
    regret_weights = compute_regret_weighted_strategy(actions, counterfactuals)
    action_ids = list(regret_weights.keys())
    propensities = list(regret_weights.values())
    forecasted_values = forecast_rewards(action_values, K, 1)
    expected_rewards = [forecasted_values[i] for i in range(len(actions))]
    confidence_bounds = [math.sqrt(1 / (1 + regret_weights[action_id])) for action_id in action_ids]
    action_index = np.argmax(expected_rewards)
    return BanditAction(action_ids[action_index], propensities[action_index], 
                        expected_rewards[action_index], confidence_bounds[action_index], "hybrid")

def hybrid_step(actions: list[MathAction], counterfactuals: list[MathCounterfactual], 
                action_values: np.ndarray, K: np.ndarray, rewards: list[float]) -> None:
    action = hybrid_select_action(actions, counterfactuals, action_values, K)
    action_values[action.action_id] = rewards[action.action_id]

