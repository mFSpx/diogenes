# ----------------------------------------------------------------------
def allocate_hybrid(
    *,
    total_units: float,
    date: dt.date,
    deterministic_target_pct: float = 90.0,
    groups: Tuple[str, ...] = GROUPS,
) -> Dict[str, Any]:
    """
    Allocate ``total_units`` between deterministic work and LLM residuals,
    then distribute the LLM portion across ``groups`` using a weekday‑dependent
    weight vector.

    The deterministic portion follows parent A exactly:
        deterministic_units = total_units * deterministic_target_pct / 100
        llm_units           = total_units - deterministic_units

    The LLM distribution uses the weight vector from ``weekday_weight_vector``:
        lane_i.llm_units = llm_units * w_i(dow)

    Returns a dictionary mirroring parent A's schema but enriched with calendar
    metadata (weekday and weight vector).
    """
    if total_units <= 0:
        raise ValueError("total_units must be positive")
    if not (0.0 <= deterministic_target_pct <= 100.0):
        raise ValueError("deterministic_target_pct must be between 0 and 100")
    if not groups:
        raise ValueError("groups required")

    # --- deterministic / LLM split (parent A) ---
    deterministic_units = total_units * deterministic_target_pct / 100.0
    llm_units = total_units - deterministic_units

    # --- calendar bridge (parent B) ---
    dow = doomsday(date.year, date.month, date.day)  # 0=Sunday…6=Saturday
    weight_vec = weekday_weight_vector(groups, dow)  # shape (N,)

    # --- per‑group allocation (linear transformation) ---
    llm_per_group = llm_units * weight_vec
    share_pct_per_group = weight_vec * 100.0

    lanes = [
        {
            "group": grp,
            "llm_units": _pct(llm_per_group[i]),
            "llm_share_pct": _pct(share_pct_per_group[i]),
            "proof_required": True,
            "weekday_weight": _pct(weight_vec[i]),
        }
        for i, grp in enumerate(groups)
    ]

    # --- jzloads (mirroring parent A, with added weekday info) ---
    jzloads: list[dict[str, Any]] = [
        {
            "kind": "OBJECT",
            "id": "project2501_hybrid_workshare_policy",
            "type": "workshare_policy",
            "deterministic_target_pct": _pct(deterministic_target_pct),
            "llm_residual_pct": _pct(100.0 - deterministic_target_pct),
        },
        {
            "kind": "EVENT",
            "id": "project2501_hybrid_workshare_allocation",
            "type": "allocation_computed",
            "total_units": _pct(total_units),
            "deterministic_units": _pct(deterministic_units),
            "llm_units": _pct(llm_units),
            "date": date.isoformat(),
            "weekday": dow,
        },
    ]

    for lane in lanes:
        jzloads.append(
            {
                "kind": "EDGE",
                "from": "project2501_hybrid_workshare_policy",
                "to": f"model_group:{lane['group']}",
                "type": "ASSIGNS_LLM_RESIDUAL_QUARTER",
                "llm_units": lane["llm_units"],
                "llm_share_pct": lane["llm_share_pct"],
                "weekday_weight": lane["weekday_weight"],
            }
        )

    return {
        "schema": "lucidota.project2501.hybrid_workshare_allocation.v1",
        "total_units": _pct(total_units),
        "deterministic_target_pct": _pct(deterministic_target_pct),
        "deterministic_units": _pct(deterministic_units),
        "llm_units": _pct(llm_units),
        "date": date.isoformat(),
        "weekday": dow,
        "weight_vector": [float(v) for v in weight_vec],
        "lanes": lanes,
        "jzloads": jzloads,
    }


def summarize_hybrid_savings(
    *,
    total_units: float,
    date: dt.date,
    deterministic_target_pct: float = 90.0,
    groups: Tuple[str, ...] = GROUPS,
) -> Dict[str, Any]:
    """
    Produce a high‑level summary analogous to parent A's ``summarize_savings``,
    but also reporting the weekday‑induced variation.
    """
    plan = allocate_hybrid(
        total_units=total_units,
        date=date,
        deterministic_target_pct=deterministic_target_pct,
        groups=groups,
    )
    return {
        "schema": "lucidota.project2501.hybrid_token_savings.v1",
        "baseline_llm_units": _pct(total_units),
        "planned_llm_units": plan["llm_units"],
        "deterministic_units": plan["deterministic_units"],
        "token_savings_pct": _pct((total_units - plan["llm_units"]) / total_units * 100.0),
        "date": date.isoformat(),
        "weekday": plan["weekday"],
        "per_group_llm_units": {lane["group"]: lane["llm_units"] for lane in plan["lanes"]},
        "per_group_weekday_weight": {
            lane["group"]: lane["weekday_weight"] for lane in plan["lanes"]
        },
    }


def demo_random_allocation(
    total_units: float = 1_000_000,
    start_year: int = 2024,
    end_year: int = 2026,
    trials: int = 5,
) -> None:
    """
    Run a quick Monte‑Carlo‑style demonstration: for a number of random dates
    within the given range, allocate workshare and print a concise report.
    This showcases the hybrid behaviour across weekdays.
    """
    for _ in range(trials):
        # random date
        year = random.randint(start_year, end_year)
        month = random.randint(1, 12)
        # ensure a valid day for the month
        day = random.randint(1, 28)  # safe for all months
        date = dt.date(year, month, day)

        alloc = allocate_hybrid(total_units=total_units, date=date)
        savings = summarize_hybrid_savings(total_units=total_units, date=date)

        print(
            f"Date {date.isoformat()} (weekday {alloc['weekday']}): "
            f"LLM units {alloc['llm_units']:,} "
            f"→ Savings {savings['token_savings_pct']}% "
            f"weights {', '.join(f'{g}:{w:.3f}' for g, w in zip(GROUPS, alloc['weight_vector']))}"
        )


