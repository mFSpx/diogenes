"""Hybrid workshare‑calendar allocator.

This module fuses **workshare_allocator.py** (deterministic vs LLM token split) with
**doomsday_calendar.py** (weekday calculation).  The mathematical bridge is a
linear transformation: the LLM residual vector is multiplied by a day‑dependent
weight vector derived from the weekday (the “doomsday”).  In matrix form

    llm_allocation = llm_total * W(dow)

where `W(dow)` is a row‑stochastic matrix (here a 1‑D weight vector) whose
entries are a sinusoidal rotation of a base pattern, ensuring the sum of
weights equals 1.  This couples the calendar topology to the token allocation
topology without merely concatenating the two algorithms."""

