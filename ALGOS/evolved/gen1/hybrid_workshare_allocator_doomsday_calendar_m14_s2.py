def doomsday(year: int, month: int, day: int) -> int:
    """
    Return weekday index where 0 = Sunday … 6 = Saturday.
    Equivalent to parent B implementation.
    """
    return (dt.date(year, month, day).weekday() + 1) % 7


def weekday_weight_vector(groups: Sequence[str], dow: int) -> np.ndarray:
    """
    Produce a normalized weight vector for *groups* based on the weekday ``dow``.
    The vector is generated by rotating a sinusoidal pattern; the rotation angle
    is proportional to ``dow`` (0‑6).  The result is stochastic (sums to 1) and
    serves as the linear operator that maps the LLM residual pool onto the
    individual group lanes.

    Mathematically:
        w_i(d) = [1 + α·sin(θ_i + φ(d))] / Σ_j[1 + α·sin(θ_j + φ(d))]
    where
        θ_i = 2π·i / N   (N = number of groups)
        φ(d) = 2π·d / 7   (weekday phase)
        α    = 0.2        (amplitude, chosen < 1 to keep positivity)

    Returns:
        np.ndarray of shape (N,) with dtype float64.
    """
    n = len(groups)
    if n == 0:
        raise ValueError("groups must contain at least one element")
    # Base angles equally spaced around the unit circle
    base_angles = np.arange(n) * (2.0 * math.pi) / n
    # Phase shift derived from weekday (0‑6 → 0‑2π)
    phase = (2.0 * math.pi) * (dow % 7) / 7.0
    amplitude = 0.2  # modest variation to keep all weights positive
    raw = 1.0 + amplitude * np.sin(base_angles + phase)
    # Normalise to make the vector stochastic
    weight_vec = raw / raw.sum()
    return weight_vec.astype(np.float64)


