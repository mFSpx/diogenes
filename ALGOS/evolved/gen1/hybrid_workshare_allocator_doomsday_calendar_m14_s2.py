GROUPS: Tuple[str, ...] = ("codex", "groq", "cohere", "local_models")


def _pct(value: float) -> float:
    """Round a float to six decimal places (parent A utility)."""
    return round(float(value), 6)


# ----------------------------------------------------------------------
# Calendar helper (parent B)
